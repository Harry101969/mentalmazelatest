// Scoring algorithms for different game types
export const calculateScore = (gameType, gameData) => {
  switch (gameType) {
    case 'memory':
      const { moves, timeSpent, pairsCount } = gameData;
      const optimalMoves = pairsCount * 2;
      const moveEfficiency = Math.max(0, 100 - ((moves - optimalMoves) * 5));
      const timeBonus = timeSpent < 300 ? 10 : 0;
      return Math.min(100, moveEfficiency + timeBonus);
      
    case 'breathing':
      const { consistency, cyclesCompleted, targetCycles } = gameData;
      const cycleCompletion = (cyclesCompleted / targetCycles) * 50;
      const consistencyScore = consistency * 0.5;
      return Math.min(100, cycleCompletion + consistencyScore);
      
    case 'puzzle':
      const { correctPlacements, totalPlacements, timeUsed, timeLimit } = gameData;
      const accuracy = (correctPlacements / totalPlacements) * 70;
      const speedBonus = timeUsed < timeLimit * 0.8 ? 20 : 10;
      return Math.min(100, accuracy + speedBonus);
      
    case 'mindfulness':
      const { challengesCompleted, totalChallenges, attentionScore } = gameData;
      const completion = (challengesCompleted / totalChallenges) * 60;
      const attention = attentionScore * 0.4;
      return Math.min(100, completion + attention);
      
    case 'selfcare':
      const { categoriesCompleted, tokensPlaced, planQuality } = gameData;
      const categoryScore = categoriesCompleted * 20;
      const tokenScore = Math.min(40, tokensPlaced * 2);
      const qualityScore = planQuality * 0.4;
      return Math.min(100, categoryScore + tokenScore + qualityScore);
      
    default:
      return 50; // Default score for unknown game types
  }
};

